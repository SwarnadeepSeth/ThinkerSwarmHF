# 1. Initialize git in your project folder
git init

# 2. Add all your files to the staging area
git add .

# 3. Save these files as your first commit
git commit -m "Initial commit"

# 4. Rename your default branch to 'main' (standard practice)
git branch -M main

# 5. Connect your local folder to your new GitHub repository
git remote add origin https://github.com/SwarnadeepSeth/ThinkerSwarmHF

# 6. Push your code up to GitHub
git push -u origin main